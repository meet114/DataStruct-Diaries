/**
 * The {@code Status} enum represents the possible status of a person in the theme park.
 * A person can be in one of the following states: Available, Holding, or OnRide.
 */
public enum Status {
  Available, // Represents that the person is available, not on any ride or in any queue
  Holding, // Represents that the person is holding in a queue, waiting for their turn to ride
  OnRide, // Represents that the person is currently on a ride
}
